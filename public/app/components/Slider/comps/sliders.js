var Slider = React.createClass({
    getInitialState() {
        return ({currentIndex:0})
    },

    next(){
        var currentIndex = this.state.currentIndex;
        if(currentIndex == 4){
            this.setState({currentIndex:0})
        } else {
            this.setState({currentIndex:currentIndex+1})
        }
    },

    back(){
        var currentIndex = this.state.currentIndex;
        if(currentIndex == 0){
            this.setState({currentIndex:4})
        } else {
            this.setState({currentIndex:currentIndex-1})
        }
    },

    render(){
        var index = this.state.currentIndex;
        var show;
        switch (index){
            case 0:
               show = <FirstSlider/>
                break;
            case 1:
                show = <SecondSlider/>
                break;
            case 2:
                show = <ThirdSlider/>
                break;
            case 3:
                show = <FourthSlider/>
                break;
            case 4:
                show = <FifthSlider/>
                break;
            default :
                show = <FirstSlider/>
                break;

        }
        return (
            <div className="slider">
                <div>
                    {show}

                    </div>
                <div className="controls">
                    <button className="arrowLeft" onClick={this.back}><i className="fa fa-chevron-left"></i></button>
                    <button className="arrowRight" onClick={this.next}><i className="fa fa-chevron-right"></i></button>
                </div>
            </div>)

    }

});


module.exports = Slider;